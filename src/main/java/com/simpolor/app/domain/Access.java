package com.simpolor.app.domain;

public class Access{

	private int access_seq;
	private String access_role;
	private String access_url;
	
	public int getAccess_seq() {
		return access_seq;
	}
	public void setAccess_seq(int access_seq) {
		this.access_seq = access_seq;
	}
	public String getAccess_role() {
		return access_role;
	}
	public void setAccess_role(String access_role) {
		this.access_role = access_role;
	}
	public String getAccess_url() {
		return access_url;
	}
	public void setAccess_url(String access_url) {
		this.access_url = access_url;
	}
	
	
}
